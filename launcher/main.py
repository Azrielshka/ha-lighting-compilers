"""
launcher/main.py
------------------------------------------------------------
Точка входа для Launcher v1.

Задача файла:
    - создать Qt-приложение
    - открыть главное окно launcher
    - запустить цикл обработки событий
"""

# ------------------------------------------------------------
# Системный модуль для аргументов и завершения приложения
# ------------------------------------------------------------
import sys

# ------------------------------------------------------------
# Qt-приложение
# ------------------------------------------------------------
from PySide6.QtWidgets import QApplication

# ------------------------------------------------------------
# Главное окно launcher
# ------------------------------------------------------------
from launcher.ui.main_window import LauncherWindow
from launcher.ui.theme import apply_theme


# ------------------------------------------------------------
# Главная функция запуска приложения
# ------------------------------------------------------------
def main():
    """
    Создаёт QApplication, инициализирует главное окно
    и запускает GUI-приложение.
    """

    app = QApplication(sys.argv)
    # До создания окон: QSS наследуется всеми, включая диалог Deploy.
    apply_theme(app)

    window = LauncherWindow()
    window.show()

    sys.exit(app.exec())


# ------------------------------------------------------------
# Запуск файла напрямую
# ------------------------------------------------------------
if __name__ == "__main__":
    main()